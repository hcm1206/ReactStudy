// Array of Javascript

// Array which contains only NUMBER
let arr1 = [1,2,3,4,5];

// Array which contains only STRING
let arr2 = ["h", "e", "l", "l", "o"];

// Array which contains NUMBER and STRING
let arr3 = [1, "h", 2, "i"];

// Array which contains VARIOUS TYPES
let arr4 = [true, 1, undefined, false, "h", 2, null, "i"];



console.log(arr1[0]); // result : 1

console.log(arr2[1]); // result : e

console.log(arr3[2]); // result : 2

console.log(arr4[3]); // result : false